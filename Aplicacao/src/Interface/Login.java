    private void Login_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login_ButtonActionPerformed
        if (this.validaDados()) {
            int resLogin=0;
            
            try {
                resLogin=this.cfacil.login(this.username.getText(),this.password.getText());
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

            if(resLogin==1) {
                this.cfacil.carregaComponentes();
                this.cfacil.carregaPacotes();
                this.cfacil.carregaConfiguracoes();
                menuVendedor = new MV_MenuVendedor(cfacil);
                menuVendedor.setTitle("Configurações");
                menuVendedor.setVisible(true);
                this.dispose();
            }
            else if (resLogin==2) {
                this.cfacil.carregaComponentes();
                this.cfacil.carregaConfiguracoes();
                menuAdmin = new MA_MenuAdmin(cfacil);
                menuAdmin.setTitle("Gestor de Sistema");
                menuAdmin.setVisible(true);
                this.dispose();
            }
            else {
                javax.swing.JOptionPane.showMessageDialog(this, "Credenciais Inválidas!", "Credenciais Inválidas!", 0);
                //this.dispose();
            }
            this.username.setText("");
            this.password.setText("");
            
        }
    }//GEN-LAST:event_Login_ButtonActionPerformed

