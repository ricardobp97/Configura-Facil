     private boolean validaDados() 
    {
        boolean vazio = this.username.getText().equals("") || this.password.getText().equals("");
        
        if (vazio)
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos.", "Formulário incompleto", 0);
        
        return !vazio;
    }
    
